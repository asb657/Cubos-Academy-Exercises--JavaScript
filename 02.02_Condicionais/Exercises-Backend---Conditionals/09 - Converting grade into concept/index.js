const grade = 10;

if (grade < 4) {
    console.log(`The concept equivalent to grade ${grade} is E.`);
} else if (grade >= 4 && grade < 6){
    console.log(`The concept equivalent to grade ${grade} is D.`);
} else if (grade >= 6 && grade < 8){
    console.log(`The concept equivalent to grade ${grade} is C.`);
} else if (grade >= 8 && grade < 9){
    console.log(`The concept equivalent to grade ${grade} is B.`);
} else if (grade >= 9){
    console.log(`The concept equivalent to grade ${grade} is A.`);
}

